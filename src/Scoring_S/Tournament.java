    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       // TODO add your handling code here:
        tournamentTeam[0] = Double.parseDouble(jTxtTeam1.getText());
        tournamentTeam[1] = Double.parseDouble(jTxtTeam2.getText());
        tournamentTeam[2] = Double.parseDouble(jTxtTeam3.getText());
        tournamentTeam[3] = Double.parseDouble(jTxtTeam4.getText());
        tournamentTeam[4] = Double.parseDouble(jTxtTeam1Cycling.getText());
        tournamentTeam[5] = Double.parseDouble(jTxtTeam2Cycling.getText());
        tournamentTeam[6] = Double.parseDouble(jTxtTeam3Cycling.getText());
        tournamentTeam[7] = Double.parseDouble(jTxtTeam4Cycling.getText());
        
        
        
        
        scoreTotal = tournamentTeam[0] + tournamentTeam[1] + tournamentTeam[2] + tournamentTeam[3] +
                tournamentTeam[4] + tournamentTeam[5] + tournamentTeam[6] + tournamentTeam[7];
        
        team1ScoreTotal= tournamentTeam[0] + tournamentTeam[4];
        team2ScoreTotal= tournamentTeam[1] + tournamentTeam[5];
        team3ScoreTotal= tournamentTeam[2] + tournamentTeam[6];
        team4ScoreTotal= tournamentTeam[3] + tournamentTeam[7];
        
                
               
        
        String tournamentScores = String.valueOf(scoreTotal);
        String tournamentTeam1 = String.valueOf(team1ScoreTotal);
       jTxtAreaTotal.setText("Team 1  scores are :\t"+ tournamentTeam1 
               + "\nTeam 2  scores are :\t" + team2ScoreTotal 
               + "\nTeam 3  scores are :\t" + team3ScoreTotal 
               + "\nTeam 4  scores are :\t" + team4ScoreTotal
               + "\nTotal Team scores are :\t" + tournamentScores);
       
 
               jTxtAreaTotal.setEnabled(true);

      
    }//GEN-LAST:event_jButton2ActionPerformed

